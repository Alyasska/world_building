export const supportedUiLocales = ['ru', 'en'] as const;

export type UiLocale = (typeof supportedUiLocales)[number];

function isUiLocale(value: string | undefined): value is UiLocale {
  return supportedUiLocales.includes(value as UiLocale);
}

export function resolveUiLocale(value: string | undefined): UiLocale {
  return isUiLocale(value) ? value : 'ru';
}

